1. Git Init
the first command your need to run in any new project that is not already a Git repository.
it can be used to convert existing, un-versioned folder into a Git repo.
Also, you can use it to initialize an empty repo.

Using Git Init Command

cd into the dir you want to initialize.

then run this command.

git init

this will transform the current dir into a Git repo. A .git sub-dir will be added.
